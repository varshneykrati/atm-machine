package com.atm.atmmachine.dto;

public class EmailDto {

	private String emailId;

	public EmailDto() {
		super();
	}

	public EmailDto(String emailId) {
		super();
		this.emailId = emailId;
	}

	public String getEmailId() {
		return emailId;
	}

	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	
	
}
